        <h6>不是撤離，而是穿越敵境</h6>
        <p>秘密大營救的困難，在於它不是普通撤離。被營救者要從香港島或九龍市區出發，穿過日軍控制區，避開崗哨、巡邏、特務和土匪，再經新界山路或海路轉入游擊區。路上可能遇到宵禁、搜查、語言不通、體力不支、天氣惡劣和臨時敵情變化。每一段路，都需要嚮導、交通員、接應點和護送隊伍配合。</p>
        <p>營救工作之所以能成功，靠的是一張隱蔽而精密的網絡。東線可由九龍經牛池灣、九龍坳、西貢，再從企嶺下或深涌灣渡海前往大鵬灣；西線則可由九龍經荃灣、大帽山、元朗、落馬洲，渡過深圳河前往白石龍游擊根據地。彩色圖片資料中亦保存了秘密大營救主要路線示意圖，並標出伶仃洋、大鵬灣、沙魚涌、落馬洲、元朗、大帽山、荃灣、九龍、西營盤、長洲等相關地點。</p>

        <h6>廖承志、連貫與喬冠華的東線脫險</h6>
        <p>秘密大營救中，廖承志、連貫、喬冠華等人是較早撤離香港的重要人物。他們的脫險，對後續營救路線和安排具有示範作用。資料記載，1942 年元旦，為秘密大營救開路而先行撤離的廖承志、連貫和喬冠華，曾在西貢企嶺下海灣登船，渡海前往沙魚涌。</p>
        <p>這段路線並不安全。被營救者需要先離開市區，經過牛池灣、九龍坳等地，再進入西貢山路和海灣。護送人員要安排接頭、化裝、警戒和登船時間，還要避開日軍巡邏艇與土匪。企嶺下海灣在這裡不只是一個地點，而是一道生死關口：只要成功登船，就有機會從日軍控制的香港進入游擊區；一旦暴露，整批人都可能被捕。</p>

        <h6>茅盾、鄒韜奮與西線山路</h6>
        <p>另一條重要路線，是經荃灣、大帽山、元朗和落馬洲的西線。茅盾、鄒韜奮等文化人曾由這條路線撤離。資料中提到，元朗十八鄉楊家村的「適廬」曾是秘密大營救的中轉站，也曾是港九大隊元朗中隊的活動據點；業主楊竹南大力支持游擊隊活動。</p>
        <p>西線艱難之處，在於要長途跋涉穿過山路和鄉村。許多文化人原本不習慣這樣的行程，途中需要有人帶路、接應、安排住宿和補給。畫家蔡迪支、許欽松後來創作的版畫《曉風殘月》，刻畫的正是 1942 年 1 月秘密大營救西線行進途中的情景；作品中前方背包袱者為茅盾，稍後戴眼鏡者為鄒韜奮。這類圖像讓後人得以想像，文化人如何在黎明前的山路上，跟隨護送隊伍一步步走出危城。</p>

        <h6>長洲與海上西線</h6>
        <p>除了東線和西線，也有部分人士經海路撤離。長洲曾是秘密大營救海上西線的重要中轉地。對一些年老體弱、難以長途翻山越嶺，或身份容易暴露的人而言，海路雖然同樣危險，卻有時是較可行的選擇。由香港島出發，經長洲、澳門，再轉往內地，是部分被營救者的脫險路線。</p>
        <p>海路營救需要船家、接應點、風向、潮汐和保密安排配合。一艘小船可能承載著一批人的性命；一個碼頭、一間魚欄、一個臨時落腳點，都可能成為救援鏈條中的關鍵。這也顯示，香港的救援故事不只發生在山路，也發生在海面之上。日軍雖控制城市和港口，但只要仍有人熟悉水道、願意冒險，海上仍可能成為逃生之路。</p>

        <h6>無一傷亡的「偉大搶救」</h6>
        <p>秘密大營救最令人動容之處，在於它在極困難的條件下成功護送大批人士脫險。資料指出，營救過程面對經費不足、敵情變化、與外省文化人語言不通等困難，但地下黨組織和武工隊仍想盡辦法解決，排除土匪干擾，闖過日軍崗哨和搜查，把大批抗日文化人及民主人士無一傷亡地護送到大後方。茅盾稱這場營救是「抗戰以來……最偉大的搶救工作」。</p>
        <p>這一評價不只是對行動規模的讚許，也說明它在文化和政治上的意義。被救出的許多人後來繼續在新聞、文學、教育、政治和抗戰宣傳中發揮作用。秘密大營救不只是救出了生命，也保存了中國抗戰中的重要文化力量。</p>

        <h6>賴濂士中校：從集中營到游擊區</h6>
        <p>港九大隊及其前身武工隊也營救了盟軍人員。1942 年 1 月，英國軍官賴濂士中校等四人從集中營逃脫後，得到港九大隊前身的一支武工隊營救。這次營救促成英軍和廣東人民抗日游擊總隊之間的合作。</p>
        <p>賴濂士的脫險具有重要後續影響。他逃出香港後，推動英方成立英軍服務團，專門從事營救戰俘和情報工作。港九大隊與英軍服務團後來建立交通站、合作收集情報，形成持續的聯絡關係。換言之，這次救援不是一次孤立事件，而是開啟了港九大隊與英方合作的重要通道。</p>

        <h6>從救援到合作：英軍服務團的連結</h6>
        <p>營救賴濂士之後，港九大隊與英軍服務團的合作逐步展開。1942 至 1943 年間，為配合英軍服務團收集情報，港九大隊國際工作組黃作梅在九龍砵蘭街開設一間雜貨鋪，作為地下交通站；戰後，英國國王授予黃作梅 MBE 勳章，以表彰他配合盟軍作戰的貢獻。</p>
        <p>這條合作線索，讓我們看到救援故事如何延伸出情報故事。當逃亡者被成功護送到游擊區，他們帶出的不只是個人生命，也可能帶來新的合作機會、新的情報渠道和新的國際聯繫。香港雖已淪陷，但透過這些救援行動，仍然與盟軍戰場保持連接。</p>

        <h6>克爾中尉：山洞中的等待</h6>
        <p>1944 年 2 月，美軍飛行員克爾中尉跳傘逃生後，日軍展開大規模搜捕。港九大隊設法保護他轉移和隱蔽。成立八十周年紀念特刊記載，1944 年 2 月，港九大隊營救跳傘逃生的美軍飛行員克爾中尉，並促成美軍與東江縱隊合作。</p>
        <p>這次救援充滿危險。克爾是外國飛行員，外貌容易暴露，日軍又高度重視搜捕。港九大隊要先把他從危險地帶轉移，再安排藏身、護送和翻譯聯絡。相關史料記載，劉黑仔等曾護送克爾轉移，並安排游擊隊員陪同他在石壟仔村的山洞藏匿兩個星期。這段山洞中的等待，正是救援工作最緊張的時刻：不能冒進，也不能久留；既要避開搜捕，也要等待安全轉移時機。</p>

        <h6>「圍魏救趙」式的營救</h6>
        <p>克爾營救之所以成功，不只靠藏匿，也靠牽制。港九大隊為分散日軍注意力，採取一系列外線行動，包括打擊漢奸、偷襲啟德機場、在市中心散發抗日傳單等，使日軍無法把全部力量集中於搜捕克爾。這類做法可稱為「圍魏救趙」式的營救：不是直接硬闖包圍，而是在其他地方製造壓力，迫使敵人分散。</p>
        <p>這種救援方式說明，營救並不只是「把人藏起來」或「把人送走」。它需要情報、判斷、佯動、戰鬥、交通和心理壓力配合。克爾之所以能脫險，是因為港九大隊能把游擊戰、情報網絡和海上交通結合起來，使日軍雖大力搜捕，仍無法截斷所有生路。</p>

        <h6>從香港到東江司令部</h6>
        <p>克爾最終被護送到東江縱隊司令部，並與東江縱隊司令員曾生會面。成立八十周年紀念特刊保存了曾生在土洋村東縱司令部送別克爾中尉的相關照片說明。這張照片背後，是一條由香港山洞、游擊隊據點、海上路線和內地根據地組成的救援通道。</p>
        <p>克爾獲救後，美軍與東江縱隊合作加深。多年後，克爾的兩個兒子曾到訪原東江縱隊港九獨立大隊老游擊戰士聯誼會，感謝港九大隊當年營救他們的父親。這一後續，讓救援故事跨越了戰爭本身，成為家族記憶、國際記憶與香港抗戰記憶之間的連結。</p>

        <h6>外籍人士的救援成果</h6>
        <p>港九大隊的救援工作不只限於幾位著名人物。據不完全統計，日軍侵佔香港期間，共有 89 名外籍人士獲救，包括英國、印度、丹麥、挪威、俄國、菲律賓和美國等國籍人士。除五名美國飛行員由東江縱隊護航大隊在大亞灣營救外，其餘因被囚禁於九龍或香港島的人士，均由港九大隊負責營救，使他們順利抵達游擊區，再轉往中國內地。</p>
        <p>這組數字說明，港九大隊的救援工作具有國際性。香港作為被佔領的國際城市，困住了不同國籍的人；而港九大隊的救援，則把這些人從日軍控制之下帶回盟軍和中國抗戰體系。救援不是附帶工作，而是港九大隊在世界反法西斯戰爭中最重要的貢獻之一。</p>

        <h6>救援中的普通人</h6>
        <p>每一次成功救援背後，都有許多普通人參與。有人提供住處，有人帶路，有人準備船隻，有人提供糧食，有人傳遞消息，有人負責觀察敵情。這些人可能是村民、漁民、交通員、婦女、商戶或地下工作者。若沒有他們，港九大隊即使有武裝，也無法完成長距離、跨地區、跨語言和跨身份的營救。</p>
        <p>救援行動最需要的，是可靠。可靠的屋主不會告密，可靠的船家不會臨陣退縮，可靠的交通員不會走錯路，可靠的接頭人會在正確時間出現。這些普通人的可靠，讓被追捕者能從一個安全點走到下一個安全點，最終走出危險。救援故事因此也是民間互助故事的延伸。</p>

        <h6>救援背後的風險與代價</h6>
        <p>救援工作從來不是安全的。藏匿一名被追捕者，可能牽連全家；護送一批文化人，可能在山路遇到日軍和土匪；接應盟軍人員，可能因語言和外貌增加暴露風險；開設地下交通站，可能一旦被破獲便全家遭難。黃作梅在九龍砵蘭街開設交通站的經歷，便說明情報與救援工作如何把個人和家庭都推向危險邊緣。</p>
        <p>正因如此，救援故事不能只看成功結果，也要看過程中的代價。許多參與者沒有留下名字，有些人被捕，有些人遭酷刑，有些家庭受到牽連。每一個「成功脫險」背後，都可能有一群人長時間承受恐懼、沉默和犧牲。</p>

        <h6>救援的意義</h6>
        <p>港九大隊的救援故事，讓我們看到抗戰中的另一種勇敢。它不是只在戰場上擊退敵人，而是在敵人控制的城市裡，把被追捕的人一個一個救出來。它需要的不只是槍械，也需要耐心、記憶力、語言能力、地形知識、臨場判斷和對人的承擔。</p>
        <p>秘密大營救保存了中國抗戰中的重要文化力量；營救賴濂士促成英軍服務團和游擊隊合作；營救克爾促進美軍與東江縱隊合作；營救 89 名外籍人士，則讓香港敵後抗戰與世界反法西斯戰爭直接連接。相關資料亦指出，港九獨立大隊卓有成效的營救盟軍、國際友人和美國飛行員等工作，擴大了其國際影響。</p>

        <h6>小結：穿越封鎖的生命線</h6>
        <p>這些救援故事，讓我們看見日佔香港並不是一座完全被封死的城市。即使日軍控制道路、碼頭、港口和市區，仍有人在山路、海灣、村屋和交通站之間，悄悄連起一條條生命線。每一次成功脫險，都不是偶然，而是無數人接力掩護、帶路、傳信和等待的結果。</p>
        <p>救援的意義，也不只在於把人送到安全地方。它保存了文化力量，促成了盟軍合作，也讓世界看見香港敵後抗戰的勇氣與能力。在封鎖最嚴密的時刻，這些行動證明了：只要仍有人願意冒險相助，黑暗之中就仍有道路可走。</p>
