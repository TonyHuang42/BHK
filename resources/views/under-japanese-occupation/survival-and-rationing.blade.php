        <h6>米糧配給與每日限量</h6>
        <p>為控制糧食，日本軍政府推行米糧配給制度。政府檔案處展覽資料記錄，1942 年每人每日只獲配給 6.4 両米，並由軍政府發出「米糧配給票」，在港島和九龍實施配給；同年《香港日報》亦報道，港島只有 57 間指定米商可售米。</p>
        <p>這種配給量遠不足以維持正常生活。市民即使取得配給票，也未必能順利買到足夠食物；排隊時間漫長，米質參差，供應亦可能中斷。對體力勞動者、老人、兒童和病人而言，長期吃不飽會迅速削弱身體。米糧配給制度表面上給予市民最低限度保障，實際上卻讓飢餓制度化，令每個人每日都被迫在不足之中計算生存。</p>

        <h6>排隊、等候與不確定</h6>
        <p>配給制度改變了日常時間。市民為了買米、領取物資或打探供應消息，常常需要排隊等候數小時。政府檔案處展覽引用一份 1942 年英方軍事情報報告，指出日佔初期香港糧食短缺嚴重，許多人靠喝粥充飢，並要排隊數小時購米。</p>
        <p>排隊本身成為日治生活的一部分。它消耗體力，也製造焦慮：輪到自己時還有沒有米？配給站會否突然關門？證件是否合格？價格會否再漲？在這種不確定中，市民不再能按正常節奏生活。每一天都圍繞食物安排，能否買到米，往往決定一家人當晚能不能開飯。</p>

        <h6>人口遣返：減少城市負擔的殘酷政策</h6>
        <p>糧食不足之下，日本軍政府推行「人口遣返」，要求或迫使大量居民離開香港返回內地。港九大隊史料提到，日軍攻佔香港後糧食、燃料供應短缺，日佔政府下令驅逐 100 萬居民離港，難民可經元朗到深圳回鄉。 政府檔案處資料亦指出，嚴重糧食短缺下，日本軍政府實施「人口遣返」。</p>
        <p>人口遣返並非簡單的返鄉安排。很多人是在戰亂、飢餓和失業中被迫離港，沿途缺乏食物、交通和安全保障。家庭可能被迫分散，老人和兒童尤其難以承受長途跋涉。對佔領政府而言，遣返可以減輕糧食壓力；對普通人而言，卻是一次被迫離開家園、踏上未知前路的生存抉擇。</p>

        <h6>從城市到鄉村：求食路線</h6>
        <p>在配給不足和遣返壓力下，很多市民被迫離開市區，前往新界、內地或親戚所在的鄉村尋找食物。城市原本是工作和生活中心，戰時卻因缺糧、失業和管制而變得難以生存。鄉村雖然同樣困苦，但至少可能有田地、山野、柴草和親族支援。</p>
        <p>這種人口流動改變了香港的社會面貌。戰前熱鬧的街區變得冷清，商舖關門，住戶離散；新界鄉村則承受更多求生者和難民壓力。有人以勞力換食物，有人投靠親友，有人沿途乞討。生存不再只是個人努力，而取決於是否有地方可去、是否有人接濟、是否能避開檢查與搶掠。</p>

        <h6>黑市與交換</h6>
        <p>當官方配給不足，黑市便成為許多人不得不面對的現實。米、油、鹽、柴、藥物、布匹和日用品，都可能在黑市以高價交易。對有積蓄或財物的人來說，還可以用首飾、衣物、家具或外幣換取食物；對貧困家庭來說，則很快陷入無物可換、無錢可買的境地。</p>
        <p>黑市並不只是貪婪的象徵，也反映官方供應失效後的求生秩序。有人靠黑市牟利，有人靠黑市活命。當配給制度無法滿足基本需求，市民只能在合法與非法、風險與飢餓之間作選擇。這種交換經濟，也將戰前的財產逐步消耗：一隻戒指、一件棉衣、一套傢俱，可能最後只換來幾天口糧。</p>

        <h6>軍票與購買力崩潰</h6>
        <p>日治時期，日本軍政府強迫市民使用軍票，並要求港幣兌換成軍票。政府檔案處指出，居民被迫把港幣兌換成軍票，這實質上是以變相方式奪取私人財產。 對市民而言，貨幣制度的改變直接影響生存能力：手上的港幣和儲蓄不再可靠，新的軍票又缺乏穩定信任和購買力。</p>
        <p>當物資短缺遇上貨幣貶值，生活壓力便更加沉重。即使手上有錢，也未必買得到食物；即使能買，價格也可能不斷上升。貨幣失去穩定價值後，實物變得更重要。米、柴、油、藥、衣物，甚至一張可通行的證件，都比紙幣更能保障生存。</p>

        <h6>柴薪、燃料與煮食困難</h6>
        <p>吃飯不只需要米，也需要燃料。日治時期，柴薪和燃料同樣短缺。政府檔案處展覽資料提到，日佔期間連柴薪供應也十分匱乏。 對普通家庭而言，即使取得少量米糧，若沒有柴炭，也難以煮食；因此，找柴、節省燃料、合煮食物，成為日常生活的一部分。</p>
        <p>燃料短缺改變了家庭煮食方式。人們盡量煮稀粥、一次煮多餐，或改用可快速烹調的食物。有人上山拾柴，有人拆取木板，有人用廢料生火。城市生活因此變得更加艱難：高樓住戶缺乏燃料來源，貧民家庭更容易陷入有米無柴、有柴無米的困境。</p>

        <h6>屋頂種菜與自救</h6>
        <p>在糧食短缺下，市民也以各種方式自救。政府檔案處展覽資料記錄，日佔期間食物匱乏，市民被迫在天台種菜維持生活。 這些小規模種植未必能真正解決飢餓，卻反映出人在極端困境中的求生本能。</p>
        <p>天台、空地、院落、山邊和村旁，都可能被用來種植蔬菜。對城市居民而言，這是一種把戰前都市生活轉向半農耕求生的方式。小小一片菜地，可能只能補充少量食物，卻能為家庭帶來一點希望。它也提醒我們，日治時期香港的生存史，不只由政策和數字構成，也由市民一日一日尋找食物的努力構成。</p>

        <h6>疾病、營養不良與死亡</h6>
        <p>長期飢餓會削弱身體，使疾病更容易蔓延。日治時期醫療資源不足，藥物短缺，醫院和診所運作受限，許多病人得不到適當治療。營養不良、傳染病、傷病和衛生惡化共同威脅市民生命。政府檔案處亦指出，日佔期間拘留營內環境惡劣，被拘禁者營養不良並受疾病困擾。</p>
        <p>鄉村也同樣艱難。《港九大隊志》記載，日佔時期赤徑村民生活艱苦、糧食不足，曾有兩人因此飢餓至死；即使如此，村民仍把僅有米糧獻出支持游擊隊。 這類記錄讓飢餓變得具體：它不是抽象的「糧食短缺」，而是真正造成死亡，也迫使人們在自身困苦和支援他人之間作艱難選擇。</p>

        <h6>難民、弱者與家庭破碎</h6>
        <p>配給制度和人口遣返對不同人群造成不同影響。年輕力壯者或許能離港尋找生路，或以勞力換取食物；但老人、兒童、病人、孕婦和孤身者往往更難生存。家庭一旦失去收入來源，便可能很快耗盡積蓄和可交換物品。有人被迫送走孩子，有人投靠親戚，有人失散後再也無法重聚。</p>
        <p>在這種環境下，家庭成為最基本的求生單位。每一口飯如何分配，誰先吃、誰少吃，誰留下、誰離開，都可能成為痛苦決定。日治香港的苦難，不只是宏觀人口下降或物價上升，也包括無數家庭在飢餓面前作出的沉默犧牲。</p>

        <h6>配給制度下的尊嚴</h6>
        <p>生存與配給最殘酷的地方，在於它使人被迫為最基本的食物低頭。排隊、查證、等待、被呵斥、被拒絕、被迫離開城市，都是佔領秩序的一部分。市民的尊嚴在一次次求米、求柴、求藥和求通行中被磨損。</p>
        <p>然而，求生並不等於屈服。許多人在極端困境中仍盡力照顧家人、接濟鄰里、支援抗日力量，或保存對未來的希望。赤徑、大浪等村民在自身缺糧下仍協助游擊隊，便說明生存壓力沒有完全摧毀互助與信念。 在日治香港，抵抗有時是拿起武器，有時則是在飢餓中仍願意把一點米分給更需要的人。</p>

        <h6>小結：飢餓中的香港</h6>
        <p>日治下的配給制度，讓香港市民每日都被迫面對生存邊界。糧食短缺、米糧限量、人口遣返、黑市交易、軍票貶值、燃料不足和疾病蔓延，共同構成日佔歲月中最切身的苦難。對許多人而言，戰爭不是遠方的軍事行動，而是飯碗裡越來越少的米，是排隊數小時仍買不到糧，是被迫離開家園尋找生路。</p>
        <p>這段歷史提醒我們，佔領統治的傷害不只來自槍炮和監獄，也來自飢餓、匱乏和每日生活的消耗。香港人在日治時期的生存，不是理所當然，而是一場長期、艱難、充滿失去與堅持的掙扎。</p>
