    <h6>淪陷歲月中的抗日武裝</h6>
    <p>1941年12月25日，香港在「黑色聖誕」中淪陷。正規防線瓦解後，香港並沒有因此完全沉默。在日軍佔領之下，一支由中國共產黨領導、活躍於港九新界和周邊海域的抗日武裝力量，逐步承擔起敵後抵抗的重任。這支隊伍，就是後來廣為人知的港九大隊，亦稱東江縱隊港九獨立大隊。</p>
    <p>港九大隊的歷史地位十分特殊。它不是外來戰場上的一支普通部隊，而是在香港淪陷的特殊環境中建立、發展和堅持下來的本地抗日力量。相關紀念特刊記載，英軍投降後，為適應香港敵後抗戰的特殊環境、便於統一指揮，廣東人民抗日游擊總隊決定將進入新界的多支武工隊合併成為「港九大隊」；1942年2月3日，港九大隊在西貢黃毛應村宣告成立。</p>

    <h6>從武工隊到港九大隊</h6>
    <p>港九大隊的成立，並非從零開始。早在日軍進攻香港初期，廣東人民抗日游擊隊第三大隊和第五大隊已派出多支武工隊進入新界。這些武工隊的任務包括發動群眾、建立抗日據點、剿匪鋤奸、收集武器，以及參與營救滯港文化人和民主人士。換言之，港九大隊成立以前，香港敵後抗戰的基礎已在新界鄉村和山區逐步形成。</p>
    <p>這些早期武工隊熟悉鄉村地形，亦依靠本地村民、漁民、青年和地下交通網絡提供支援。它們活動於西貢、沙頭角、元朗、大嶼山等地，既要面對日軍，又要處理土匪、偽警、特務和糧食補給等問題。到1942年初，分散的武工隊需要更統一的領導和更清晰的組織架構，港九大隊遂在這樣的背景下正式成立。</p>
    <p>港九大隊的成立，是香港敵後抗戰從分散行動走向建制化的重要轉折。它將原本分散在不同地區的武裝和地下力量整合起來，逐步形成大隊部、中隊、短槍隊、海上隊、市區隊和交通情報系統等不同分工。這種組織化，使港九大隊能夠在日軍高壓管治下長期活動，而不是只進行短暫、零散的抵抗。</p>

