    <h6>在危難中打開生路</h6>
    <p>日佔香港的歷史中，有許多故事不是以戰鬥開始，而是以「救人」開始。有人被困在淪陷後的市區，有人從集中營逃出，有人在飛機墜落後被日軍搜捕，也有人因身份、職業或政治立場而面臨生命危險。在日軍封鎖交通、實施宵禁、搜捕抗日人士的情況下，要把人安全送出香港，往往比一場戰鬥更漫長、更細緻，也更危險。</p>
    <p>這些救援故事，展示了港九大隊及其前身武工隊的另一面。它們不只是抗日武裝，也是生命通道的守護者。從秘密大營救到營救英軍戰俘，從保護美軍飛行員到協助國際人士脫險，港九大隊把山路、海灣、村屋、交通站和小船連接起來，在封鎖之中為被追捕的人打開生路。</p>

    <h6>秘密大營救：淪陷後的第一場搶救</h6>
    <p>1941 年 12 月 25 日香港淪陷後，大批滯留香港的抗日文化人和民主人士陷入危險。日軍封鎖交通、搜捕抗日人士，並試圖逼迫知名文化人報到或合作。許多人來自外省，不熟悉香港地形和語言，也缺乏安全藏身之處。若不盡快轉移，他們隨時可能被捕、被殺，或被迫替日軍宣傳服務。</p>
    <p>在這樣的背景下，中共地下黨組織、廣東人民抗日游擊隊及港九大隊前身的武工隊展開了「秘密大營救」。這場行動從 1941 年 12 月 25 日香港淪陷開始，到 1942 年 11 月 22 日鄒韜奮到達蘇北抗日根據地為止，歷時 11 個月；先後救出知名抗日民主人士和文化人 300 多人，連同其他人士共約 800 人。</p>

