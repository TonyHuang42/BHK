    <h6>在短缺中求存</h6>
    <p>日治下的香港，最直接、最沉重的問題之一，是如何活下去。戰前的香港依賴進口糧食和區域貿易維持城市供應；日軍佔領後，交通受阻、貿易中斷、物資被徵用，原有供應系統迅速崩潰。市民不只面對政治高壓，也要面對每日最基本的問題：有沒有米、有沒有柴、有沒有藥、明天還能不能吃飽。</p>
    <p>政府檔案處關於日佔香港的展覽資料指出，日軍佔領期間香港居民面對糧食和日用品嚴重短缺；在嚴重糧荒之下，日本軍政府推行「人口遣返」和嚴格米糧配給，市民亦被迫把港幣兌換成軍票。 這些政策表面上是管理糧食和人口，實際上使市民的生存更加依附於佔領政府。要取得糧食，就要登記、排隊、配給、服從規則；而沒有穩定工作、證件或社會支援的人，往往最先被推向飢餓和流離。</p>

    <h6>糧食危機的形成</h6>
    <p>香港本來不是一個能自給自足的城市。它人口密集，農地有限，糧食長期依靠外地輸入。日軍佔領後，海上交通、內地供應和國際貿易都受到戰爭影響；同時，日軍和佔領機構又優先掌握物資，令普通市民的食物供應急劇惡化。戰時香港的糧食問題，不只是自然短缺，更是戰爭封鎖、軍事徵用和佔領管治共同造成的結果。</p>
    <p>城市一旦失去穩定供應，市民生活便迅速倒退。米價高漲，副食品難求，肉類、油鹽、糖、柴炭和藥物都變得珍貴。很多家庭不得不減少餐數，將米煮成稀粥，或以番薯、野菜、樹葉、雜糧充飢。飢餓不只是身體感受，也改變了家庭關係和社會秩序：人們為一小袋米排隊、交換物品、尋找親友接濟，甚至被迫離開城市。</p>

