        <h6>黃毛應村與西貢根據地</h6>
        <p>港九大隊的成立地點，與西貢密切相關。1942年2月3日，港九大隊在西貢黃毛應村宣告成立。今日黃毛應村的玫瑰小堂，常被視為與港九大隊早期活動有關的重要遺址。古物古蹟辦事處的大灣區東江縱隊文物徑資料指出，由於黃毛應村戰略位置優越，港九獨立大隊曾以村內玫瑰小堂作為游擊隊指揮中心及大隊部常駐地之一；村中青年亦積極參與抗日，並有民兵、青年會、婦女會等群眾組織協助游擊隊行動。</p>
        <p>西貢之所以重要，與它的地理環境有密切關係。西貢既有山地、村落和海灣，也能連接九龍、新界東北、大鵬灣和內地游擊區。對日軍而言，這些山海交錯的地帶難以完全控制；對港九大隊而言，卻提供了隱蔽、轉移、補給和海上交通的條件。西貢因此成為港九大隊早期活動的重要基地，也成為香港敵後抗戰記憶中最具代表性的地區之一。</p>
        <p>黃毛應村的意義，不只在於它是一個成立地點。它象徵著港九大隊與本地村落社會之間的密切關係。港九大隊能夠在日佔香港生存和發展，不能單靠武裝人員自身，也需要村民提供住處、食物、情報、嚮導和掩護。這種軍民之間的互助關係，是香港敵後抗戰能夠延續的重要基礎。</p>

        <h6>香港淪陷期間唯一成建制抵抗力量</h6>
        <p>港九大隊最常被提及的歷史定位，是它在香港淪陷三年零八個月期間，成為唯一一支成建制、由始至終堅持抵抗的抗日武裝力量。這一點在多份港九大隊相關史料中均有明確記載。香港歷史博物館亦在「香港故事」展區介紹中指出，1941年香港淪陷後，市民飽受戰亂之苦，中國共產黨領導的抗日游擊隊港九大隊展開營救、情報與抗敵工作，為世界反法西斯戰爭作出重要貢獻。</p>
        <p>「成建制」這幾個字，對理解港九大隊十分重要。它表示港九大隊不是臨時聚合的抗日小組，也不只是零散的地方自衛隊，而是具有明確領導、部隊編制、工作分工和行動範圍的抗日武裝。它在不同地區設立中隊，按地形與任務分工活動，並透過交通站、情報員、民運工作和後勤支援維持運作。</p>
        <p>這種建制化，也使港九大隊能夠承擔多種任務。它既能作戰，也能營救；既能收集情報，也能宣傳抗日；既能在山區活動，也能進入市區；既能依靠鄉村網絡，也能利用海上交通。正因如此，港九大隊在香港抗戰史中不只是「一支游擊隊」，而是日佔時期香港敵後抵抗的核心組織。</p>

        <h6>港九大隊的組織與分工</h6>
        <p>港九大隊的組織隨戰局發展而變化。早期，它由進入新界的多支武工隊合併而成；其後因應不同地區和任務，逐步形成西貢、沙頭角、元朗、大嶼山、海上、市區等不同活動單位。這些單位並非只是地理分區，而是根據香港特殊環境形成的作戰與工作分工。</p>
        <p>西貢和沙頭角一帶，山地與鄉村較多，適合建立根據地、交通站和短槍隊活動。元朗與新界西北，連接深圳河一帶，對交通、護送和物資轉移具有重要意義。大嶼山和海上中隊，則利用島嶼與海路環境，開展海上交通、護航和海上作戰。市區中隊則深入香港、九龍市區，以情報、宣傳、地下聯絡和破壞行動為主要任務。</p>
        <p>這種分工，反映了港九大隊對香港地形與社會環境的適應。香港地方狹小，日軍駐有重兵，游擊隊難以像內地廣闊農村那樣建立大面積根據地。因此，港九大隊必須以靈活、分散、隱蔽的方式活動：山區能藏身，海上能轉移，市區能收集情報，鄉村能支援後勤。這正是香港敵後抗戰的特殊性。</p>

        <h6>成員來源與本地參與</h6>
        <p>港九大隊的成員，既有來自廣東人民抗日游擊隊的骨幹，也有大量香港本地青年、工人、學生、原居民和鄉村人士。它不是一支與香港社會隔離的外部力量，而是在本地社會支援下成長起來的抗日隊伍。港九大隊史料強調，其成員絕大部分是香港居民，包括原居民和青年學生；他們冒著生命危險投入抗日行動，體現出港人的傳統愛國精神。</p>
        <p>這種本地參與，使港九大隊的歷史具有特別意義。香港淪陷後，許多普通人面對的選擇並不容易：加入游擊隊可能招致日軍報復，協助交通或藏匿人員也可能危及全家。但仍有不少青年和鄉民選擇參與抗戰，有人拿起武器，有人負責帶路，有人提供糧食，有人傳遞情報，也有人在村內維持聯絡和掩護。</p>
        <p>因此，港九大隊的故事，不只是武裝隊員的故事，也包含許多普通香港人的選擇。正是這些看似微小的支援，使港九大隊能夠在敵後環境中延續。抗日力量並非只存在於戰鬥現場，也存在於村屋、碼頭、漁船、山徑、祠堂、學校和秘密聯絡點之中。</p>

        <h6>在戰略要地上的敵後抵抗</h6>
        <p>香港在日軍戰略中具有重要地位。相關史料指出，香港是日軍在太平洋的轉運樞紐和海軍中繼站，也是日軍北上侵略中國內地和南下侵略東南亞的重要據點。日軍甚至設立直屬大本營的「香港佔領地總督部」，可見其對香港的重視。</p>
        <p>正因香港具有這樣的戰略價值，港九大隊在香港的活動便不只是地方性抵抗。它在日軍佔領的交通、港口、機場、倉庫、海路和市區之間開展行動，實際上是在日本軍事體系的重要節點上進行干擾。港九大隊能夠掌握日軍艦艇往來、兵員調動和軍事設施情況，並將情報提供盟軍參考，這使其工作與國際反法西斯戰爭直接相連。</p>
        <p>這一點也解釋了為何港九大隊的歷史不應只被視作地方鄉土史。它的活動範圍雖在香港及周邊，但其意義連接中國抗戰和太平洋戰爭。香港是日本南進戰略中的重要據點，而港九大隊正是在這個據點內部堅持抵抗，削弱佔領秩序，保存抗戰力量，並支援盟軍情報與營救工作。</p>

        <h6>多種形式的抗日工作</h6>
        <p>港九大隊的工作並不限於武裝戰鬥。它的抗日方式十分多樣，包括游擊襲擊、爆破、鋤奸、宣傳、情報收集、護送營救、海上交通、物資轉運、後勤醫療和民眾動員等。這種多樣性，正是它能在香港這種高壓佔領環境下生存的重要原因。</p>
        <p>在軍事上，港九大隊透過小規模、機動性強的行動打擊日軍和偽組織，避免與敵人進行不利的大規模正面決戰。在情報上，它利用市區、鄉村和海上網絡收集日軍軍事設施、船隻往來和兵力調動情況。在營救方面，它曾協助文化人、民主人士和盟軍人員脫離險境。在宣傳方面，它又透過傳單和秘密印刷，鼓舞民心，打擊日軍佔領秩序。</p>
        <p>這些工作彼此相連。游擊戰需要情報支援，營救需要交通網絡，情報工作需要市區和村落掩護，海上行動需要漁民與島嶼據點配合。因此，港九大隊不能只從單一角度理解；它是一個集軍事、情報、交通、宣傳和群眾工作於一身的敵後抗日組織。</p>

        <h6>犧牲、紀念與歷史傳承</h6>
        <p>港九大隊的活動付出了沉重代價。許多隊員、交通員、民運人員和支援村民在戰鬥、搜捕、酷刑和掃蕩中犧牲。戰後，香港多處抗戰紀念設施保存了這段記憶，包括西貢斬竹灣抗日英烈紀念碑、烏蛟騰抗日英烈紀念碑，以及近年落成的劉春祥抗日英雄羣體紀念碑。古物古蹟辦事處在介紹大灣區東江縱隊文物徑時指出，文物徑的設立不僅為記錄港九大隊在抗戰期間所經歷的苦難與犧牲，也承載其保家衛國、不屈不撓的精神。</p>
        <p>這些紀念設施提醒後人，港九大隊的歷史並不是抽象的戰史，而是具體地落在香港山海與村落之中。黃毛應、赤徑、烏蛟騰、斬竹灣、沙頭角、龍鼓灘等地名，既是地理空間，也是記憶空間。它們記錄了游擊隊員的行動，也記錄了村民的支援、犧牲者的名字，以及香港在日佔時期仍未熄滅的抵抗火種。</p>

        <h6>小結：香港抗戰中的中流砥柱</h6>
        <p>港九大隊的歷史，讓我們看見香港在淪陷歲月中並非只有受苦、沉默與等待。即使正規防線已經崩潰，仍有人在山野、村落、海灣和城市之間繼續抵抗，守護同胞，支援抗戰，並把香港與更廣闊的中國抗戰和世界反法西斯戰爭連繫起來。</p>
        <p>這支隊伍之所以重要，不只因為它有武裝力量，更因為它紮根於香港本地社會。青年、工人、學生、原居民、漁民、村民和地下工作者，以不同方式參與其中，使港九大隊成為日佔香港最具代表性的抗日力量之一。</p>
        <p>今天回望港九大隊，不只是為了記住一支游擊隊的名稱，更是為了理解香港人在戰爭中展現的勇氣、互助與責任。它留下的，是一段屬於香港的抗戰記憶：在最黑暗的年代，仍有人選擇挺身而出，保衛家園，延續希望。</p>
